#include "unistd.h"

int	main(void)
{
	int id;
	char c;
	int	i;

	i = 0;
	id = fork();

	if(id == 0)
	{
		c = 'x';
		while(i < 100)
		{
			write(1,&c,1);
			i++;
		}
		write(1,"\n",1);
	}
	else
	{
		c = 'l';
		while(i < 100)
		{
			write(1,&c,1);
			i++;
		}
		write(1,"\n",1);


	}

}